// Generates src/lib/registry.ts: official Robinhood stock tokens joined with Chainlink feeds.
import { writeFileSync } from "node:fs";

const BS = "https://robinhoodchain.blockscout.com/api/v2";

async function fetchAllOfficialTokens() {
  const tokens = [];
  let params = "";
  for (let page = 0; page < 10; page++) {
    const res = await fetch(`${BS}/tokens?q=Robinhood%20Token${params}`);
    const d = await res.json();
    for (const t of d.items) {
      if ((t.name || "").includes("• Robinhood Token") && t.type === "ERC-20") {
        tokens.push(t);
      }
    }
    if (!d.next_page_params) break;
    params = "&" + new URLSearchParams(d.next_page_params).toString();
  }
  return tokens;
}

async function fetchFeeds() {
  const res = await fetch(
    "https://reference-data-directory.vercel.app/feeds-robinhood-mainnet.json",
  );
  const feeds = await res.json();
  const map = new Map();
  for (const f of feeds) {
    // Stock feeds are named "Robinhood TICKER / USD" or "Robinhood TICKER-USD"
    const m = /^Robinhood ([A-Z]+)\s*[-/]\s*USD$/.exec(f.name.trim());
    if (m) map.set(m[1], f.proxyAddress || f.contractAddress);
  }
  return map;
}

const [tokens, feeds] = await Promise.all([fetchAllOfficialTokens(), fetchFeeds()]);

const seen = new Set();
const rows = [];
for (const t of tokens) {
  const ticker = t.symbol;
  if (seen.has(ticker)) continue; // keep the first (highest holder count — list is sorted)
  const feed = feeds.get(ticker);
  if (!feed) continue; // no feed → can't compute premium
  seen.add(ticker);
  rows.push({
    ticker,
    name: t.name.replace(" • Robinhood Token", ""),
    token: t.address_hash,
    feed,
    icon: t.icon_url || null,
    holders: Number(t.holders_count || 0),
  });
}
rows.sort((a, b) => b.holders - a.holders);

const missingFeed = tokens
  .filter((t) => !seen.has(t.symbol) && !feeds.get(t.symbol))
  .map((t) => t.symbol);
console.log(`official tokens: ${tokens.length}, with feed: ${rows.length}`);
console.log(`no feed (skipped): ${[...new Set(missingFeed)].join(", ")}`);

const out = `// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit by hand.
// Official Robinhood stock tokens on Robinhood Chain mainnet, joined with
// their Chainlink price-feed proxies. Regenerate when Robinhood lists new
// tokens: node scripts/gen-registry.mjs

export interface StockEntry {
  ticker: string;
  /** Company name, e.g. "NVIDIA" */
  name: string;
  /** Official stock-token contract (ERC-20, 18 decimals) */
  token: \`0x\${string}\`;
  /** Chainlink AggregatorV3 proxy for TICKER/USD (8 decimals) */
  feed: \`0x\${string}\`;
  /** Robinhood CDN icon, if any */
  icon: string | null;
}

export const STOCKS: StockEntry[] = [
${rows
  .map(
    (r) =>
      `  { ticker: ${JSON.stringify(r.ticker)}, name: ${JSON.stringify(r.name)}, token: ${JSON.stringify(r.token)}, feed: ${JSON.stringify(r.feed)}, icon: ${JSON.stringify(r.icon)} },`,
  )
  .join("\n")}
];

export const STOCK_BY_TICKER = new Map(STOCKS.map((s) => [s.ticker, s]));
`;

const tsPath = process.argv[2] ?? "registry.ts";
writeFileSync(tsPath, out);
console.log("written", tsPath);

// Plain-data mirror of STOCKS for scripts that can't import a .ts file
// (e.g. scripts/snapshot-premiums.mjs, run standalone in CI). Keep this next
// to registry.ts so both are always regenerated together — do not hand-edit.
const jsonPath = tsPath.replace(/\.ts$/, ".json");
writeFileSync(
  jsonPath,
  JSON.stringify(
    rows.map((r) => ({ ticker: r.ticker, name: r.name, token: r.token, feed: r.feed })),
    null,
    2,
  ) + "\n",
);
console.log("written", jsonPath);
