// Copies compiled GapMarket/MockAggregator ABIs from contracts/artifacts into
// src/lib so the frontend never hand-maintains a duplicate. Re-run after any
// Solidity change: node scripts/sync-predict-abi.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

const CONTRACTS = [
  { name: "GapMarket", artifact: "contracts/artifacts/contracts/GapMarket.sol/GapMarket.json" },
  { name: "PlayMarket", artifact: "contracts/artifacts/contracts/PlayMarket.sol/PlayMarket.json" },
  { name: "MockAggregator", artifact: "contracts/artifacts/contracts/MockAggregator.sol/MockAggregator.json" },
];

let out = "// AUTO-GENERATED by scripts/sync-predict-abi.mjs from contracts/artifacts — do not edit by hand.\n\n";

for (const { name, artifact } of CONTRACTS) {
  const { abi } = JSON.parse(readFileSync(join(ROOT, artifact), "utf8"));
  out += `export const ${name.toUpperCase()}_ABI = ${JSON.stringify(abi, null, 2)} as const;\n\n`;
}

writeFileSync(join(ROOT, "src/lib/predictAbi.ts"), out);
console.log("written src/lib/predictAbi.ts");
